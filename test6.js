// 1) Write a JavaScript Function to Reverse a String.
function ReverseString(string)
{
    let reverseString = '';
    for(i= string.length -1; i>=0; i--)
    {
        reverseString += string[i]
    }
    return reverseString;
}
console.log(ReverseString("Hi this is Alaa"));


